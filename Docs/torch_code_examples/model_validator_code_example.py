""" These are code examples to be used when generating AIMET documentation via Sphinx """

import torch

# Both compression and quantization related imports
from aimet_torch.examples import mnist_torch_model

# Model Validator related imports
from aimet_torch.model_validator.model_validator import ModelValidator


def validate_mnist_model():

    # Load a trained MNIST model
    model = mnist_torch_model.Net()

    # Output of ModelValidator.validate_model will be True if model is valid, False otherwise
    print(ModelValidator.validate_model(model, model_input=torch.rand(1, 1, 28, 28)))


if __name__ == '__main__':
    validate_mnist_model()
